export interface Task {
  id: string; // Unique identifier for the task
  description: string; // Detailed description of the task
  assignedAssistant: string; // ID of the assistant assigned to this task
  status: 'pending' | 'in_progress' | 'completed' | 'failed'; // Task status
  inputData?: Record<string, unknown>; // Input data for the task
  outputData?: Record<string, unknown>; // Output data generated by the task
  createdAt: Date; // Task creation timestamp
  updatedAt: Date; // Task last update timestamp
}
